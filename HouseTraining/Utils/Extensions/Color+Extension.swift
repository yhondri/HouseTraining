import SwiftUI

extension Color {
    //MARK: - ChartView
    static let charBarTopColor = Color("ChartBarTopColor")
    static let charBarBottomColor = Color("ChartBarBottomColor")    
    static let tableViewBackgroundColor = Color("TableBackgroundColor")
    static let itemTextColor = Color("ItemTextColor")
    static let itemBackgroundColor = Color("ItemBackgroundColor")
}

extension UIColor {
    //MARK: - ChartView
    static let charBarTopColor = UIColor(named: "ChartBarTopColor")
    static let charBarBottomColor = UIColor(named: "ChartBarBottomColor")
    static let tableViewBackgroundColor = UIColor(named: "TableBackgroundColor")
    static let buttonMainColor = UIColor(named: "ButtonMainColor")
    static let itemTextColor = UIColor(named: "ItemTextColor")
    static let itemBackgroundColor = UIColor(named: "ItemBackgroundColor")
    static let swiftUIBlueColor = UIColor(Color.blue)
}
